package model.entities;

public class Vehicle {

	private String model;
	
	// Constructors
	public Vehicle() {		
	}

	public Vehicle(String model) {		
		this.model = model;
	}

	// Getters & Setters
	public String getModel() {
		return model;
	}

	public void setModel(String model) {
		this.model = model;
	}
	
	
}
